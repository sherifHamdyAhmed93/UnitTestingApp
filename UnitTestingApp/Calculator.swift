import Foundation

class Calculator{
    func add(_ a:Int , _ b:Int)->Int{
        return a + b
    }
    
    func subtract(_ a:Int , _ b:Int)->Int{
        return a - b
    }
    
    func multiply(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
}
